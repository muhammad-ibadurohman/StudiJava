public class OperasiPerbandingan {
    public static void main(String[] args) {
        int angka = 9;
        int angka2 = 12;

        System.out.println(angka > angka2 );
        System.out.println(angka < angka2 );
        System.out.println(angka >= angka2 );
        System.out.println(angka <= angka2 );
        System.out.println(angka == angka2 );
        System.out.println(angka != angka2 );


    }
}
